package com.kokoa.together.proDForm.dto;


public class ProDFormDTO {
	private int pdNo;
	private int pdtSep;
	private String pdsDate;
	private String pdeDate;
	private String pdId;
	private String pdTitle;
	private String pdContent;
	private String pdtImg;
	private String pdCop;
	
	
	public int getPdNo() {
		return pdNo;
	}
	public void setPdNo(int pdNo) {
		this.pdNo = pdNo;
	}
	public int getPdtSep() {
		return pdtSep;
	}
	public void setPdtSep(int pdSep) {
		this.pdtSep = pdSep;
	}
	public String getPdsDate() {
		return pdsDate;
	}
	public void setPdsDate(String pdsDate) {
		this.pdsDate = pdsDate;
	}
	public String getPdeDate() {
		return pdeDate;
	}
	public void setPdeDate(String pdeDate) {
		this.pdeDate = pdeDate;
	}
	public String getPdId() {
		return pdId;
	}
	public void setPdId(String pdid) {
		this.pdId = pdid;
	}
	public String getPdTitle() {
		return pdTitle;
	}
	public void setPdTitle(String pdTitle) {
		this.pdTitle = pdTitle;
	}
	public String getPdContent() {
		return pdContent;
	}
	public void setPdContent(String pdContent) {
		this.pdContent = pdContent;
	}
	public String getPdtImg() {
		return pdtImg;
	}
	public void setPdtImg(String pdtImg) {
		this.pdtImg = pdtImg;
	}

	public String getPdCop() {
		return pdCop;
	}
	public void setPdCop(String pdCop) {
		this.pdCop = pdCop;
	}
}
