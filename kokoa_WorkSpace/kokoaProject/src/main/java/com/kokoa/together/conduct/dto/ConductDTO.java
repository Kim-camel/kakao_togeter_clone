package com.kokoa.together.conduct.dto;

public class ConductDTO {
	private int coNo;
	private String coTitle;
	private String coContent;
	private String coImg;
	private String coCop;
	private String coSep;
	
	public String getCoSep() {
		return coSep;
	}
	public void setCoSep(String coSep) {
		this.coSep = coSep;
	}
	public int getCoNo() {
		return coNo;
	}
	public void setCoNo(int coNo) {
		this.coNo = coNo;
	}
	public String getCoTitle() {
		return coTitle;
	}
	public void setCoTitle(String coTitle) {
		this.coTitle = coTitle;
	}
	public String getCoContent() {
		return coContent;
	}
	public void setCoContent(String coContent) {
		this.coContent = coContent;
	}
	public String getCoImg() {
		return coImg;
	}
	public void setCoImg(String coImg) {
		this.coImg = coImg;
	}
	public String getCoCop() {
		return coCop;
	}
	public void setCoCop(String coCop) {
		this.coCop = coCop;
	}
}
